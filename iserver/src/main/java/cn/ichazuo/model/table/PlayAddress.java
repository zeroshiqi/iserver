package cn.ichazuo.model.table;

import java.util.Date;

import javax.persistence.Table;

import cn.ichazuo.commons.base.BaseModel;

/**
 * @ClassName: PlayAddress
 * @Description: (直播地址)
 * @author ZhaoXu
 * @date 2015年7月23日 下午5:11:51
 * @version V1.0
 */
@Table(name = "t_play_address")
public class PlayAddress extends BaseModel {
	private static final long serialVersionUID = 1L;

	private Long id;
	private String addressName; // 地址名称
	private String addressUrl; // 播放地址
	private String addressWebUrl;	//web播放地址

	private Integer status; // 状态
	private Long version; // 版本
	private Date createAt; // 创建时间
	private Date updateAt; // 修改时间

	
	// get set
	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getAddressName() {
		return addressName;
	}

	public void setAddressName(String addressName) {
		this.addressName = addressName;
	}

	public String getAddressUrl() {
		return addressUrl;
	}

	public void setAddressUrl(String addressUrl) {
		this.addressUrl = addressUrl;
	}

	public Integer getStatus() {
		return status;
	}

	public void setStatus(Integer status) {
		this.status = status;
	}

	public Long getVersion() {
		return version;
	}

	public void setVersion(Long version) {
		this.version = version;
	}

	public Date getCreateAt() {
		return createAt;
	}

	public void setCreateAt(Date createAt) {
		this.createAt = createAt;
	}

	public Date getUpdateAt() {
		return updateAt;
	}

	public void setUpdateAt(Date updateAt) {
		this.updateAt = updateAt;
	}

	public String getAddressWebUrl() {
		return addressWebUrl;
	}

	public void setAddressWebUrl(String addressWebUrl) {
		this.addressWebUrl = addressWebUrl;
	}

}
