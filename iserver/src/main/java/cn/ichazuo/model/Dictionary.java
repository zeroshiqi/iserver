package cn.ichazuo.model;

import cn.ichazuo.commons.base.BaseResult;

/**
 * @ClassName: Dictionary
 * @Description: (字典数据)
 * @author ZhaoXu
 * @date 2015年7月5日 下午7:22:12
 * @version V1.0
 */
public class Dictionary extends BaseResult {
	private static final long serialVersionUID = 1L;

	private Long id; // 主键
	private String value; // 值

	public Dictionary() {
	}

	public Dictionary(Long id, String value) {
		this.id = id;
		this.value = value;
	}

	// get set
	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}
}
