package cn.ichazuo.model.admin;

import cn.ichazuo.commons.base.BaseResult;

/**
 * @ClassName: UserListInfo
 * @Description: (后台用户列表信息)
 * @author ZhaoXu
 * @date 2015年7月15日 下午12:44:04
 * @version V1.0
 */
public class UserListInfo extends BaseResult {
	private static final long serialVersionUID = 1L;

	private Long id; // id
	private Long roleId; // roleId
	private String account; // 账号
	private String roleName; // 角色名称
	private String realName; // 真实姓名
	private String remark; // 备注

	//get set
	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public Long getRoleId() {
		return roleId;
	}

	public void setRoleId(Long roleId) {
		this.roleId = roleId;
	}

	public String getAccount() {
		return account;
	}

	public void setAccount(String account) {
		this.account = account;
	}

	public String getRoleName() {
		return roleName;
	}

	public void setRoleName(String roleName) {
		this.roleName = roleName;
	}

	public String getRealName() {
		return realName;
	}

	public void setRealName(String realName) {
		this.realName = realName;
	}

	public String getRemark() {
		return remark;
	}

	public void setRemark(String remark) {
		this.remark = remark;
	}

}
