package cn.ichazuo.model.admin;

import cn.ichazuo.commons.base.BaseResult;

/**
 * @ClassName: PlayAddressListInfo
 * @Description: (播放地址列表信息)
 * @author ZhaoXu
 * @date 2015年7月23日 下午6:21:20
 * @version V1.0
 */
public class PlayAddressListInfo extends BaseResult {
	private static final long serialVersionUID = 1L;
	private Long id; // id
	private String addressName; // 播放地址名称
	private String addressUrl; // url
	private String addressWebUrl;	//m3u8地址 web使用

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getAddressName() {
		return addressName;
	}

	public void setAddressName(String addressName) {
		this.addressName = addressName;
	}

	public String getAddressUrl() {
		return addressUrl;
	}

	public void setAddressUrl(String addressUrl) {
		this.addressUrl = addressUrl;
	}

	public String getAddressWebUrl() {
		return addressWebUrl;
	}

	public void setAddressWebUrl(String addressWebUrl) {
		this.addressWebUrl = addressWebUrl;
	}

}
